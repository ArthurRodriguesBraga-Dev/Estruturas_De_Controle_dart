main() {
  for (int i = 0; i <= 10; i++) {
    print("i = $i");
  }
  print("Fim do código");

  //Laço FOR que vai de 100 até 0 pulando de 4 em 4
  print("\nLaço FOR que vai de 100 até 0 pulando de 4 em 4");
  for (int i = 100; i >= 0; i -= 4) {
    print("i = $i");
  }
  print("Fim do código");
}
